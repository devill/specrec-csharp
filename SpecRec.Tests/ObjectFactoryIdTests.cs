        public void AutoGeneratedIds_ShouldFollowClassNamePattern() // "ClassName_1", "ClassName_2", etc.
        {
            var factory = new ObjectFactory();
            var obj1 = new TestService();
            var obj2 = new TestService();
            var obj3 = new AnotherService();
            
            factory.SetOne(obj1);
            factory.SetOne(obj2);
            factory.SetOne(obj3);
            
