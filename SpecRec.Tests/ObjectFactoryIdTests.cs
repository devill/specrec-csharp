            var id2 = factory.GetRegisteredId(obj2);
            var id3 = factory.GetRegisteredId(obj3);
            
            Assert.Equal("TestService_1", id1);
            Assert.Equal("TestService_2", id2);
            Assert.Equal("AnotherService_3", id3);
